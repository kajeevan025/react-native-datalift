const App = () => {
  const [results, setResults] = useState<DataLiftResponse[]>([]);
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);
  const [modelStatus, setModelStatus] = useState<string>("preparing model…");
  const [downloadProgress, setDownloadProgress] =
    useState<ModelDownloadProgress | null>(null);

  // ─── Auto-download LayoutLMv3 on mount ─────────────────────────────────
  // v1.2.4: DataLift.configure() enables auto-download; prepareModel() discovers
  // a cached model or downloads it once from GitHub Releases. extract() then
  // uses it automatically on every call.

  useEffect(() => {
    let active = true;

    DataLift.configure({
      autoDownloadLayoutLMv3: true,
      onModelDownloadProgress: (p: ModelDownloadProgress) => {
        if (!active) return;
        setDownloadProgress(p);
        setModelStatus(
          `Downloading model: ${Math.round(p.progress * 100)}% (${p.fileName})`,
        );
      },
    });

    const prepare = async () => {
      try {
        await DataLift.prepareModel();
        if (!active) return;
        setDownloadProgress(null);
        setModelStatus("ready — up to 5 stages active");
      } catch (err) {
        if (!active) return;
        const msg = err instanceof Error ? err.message : String(err);
        setDownloadProgress(null);
        setModelStatus(`OCR + parser only — ${msg}`);
      }
    };

    prepare();
    return () => {
      active = false;
    };
  }, []);

  // ─── Unified extraction — OCR → Parser → LayoutLMv3 (if configured) ────

  const processUri = useCallback(async (uri: string) => {
    setLoading(true);
    setError(null);
    try {
      // All stages run automatically inside DataLift.extract():
      //  1. OCR (Apple Vision / ML Kit)
      //  2. RuleBasedParser (payment, delivery, notes, UOM, references)
      //  3. ConfidenceEngine (5-factor breakdown)
      //  4. LayoutLMv3 (auto-configured by prepareModel)
      //  5. AIEngine fallback (if confidence < threshold)
      const result = await DataLift.extract({
        image: uri,
        language: "en",
        debug: __DEV__,
      });
      setResults((prev) => [result, ...prev]);
    } catch (err) {
      const msg = err instanceof Error ? err.message : String(err);
      setError(msg);
      Alert.alert("Extraction Error", msg);
    } finally {
      setLoading(false);
    }
  }, []);

  // ─── Camera capture ────────────────────────────────────────────────────

  const handleCapture = useCallback(async () => {
    launchCamera(
      { mediaType: "photo", cameraType: "back", quality: 1 },
      async (response) => {
        if (response.didCancel) return;
        if (response.errorCode) {
          Alert.alert(
            "Camera Error",
            response.errorMessage ?? response.errorCode,
          );
          return;
        }
        const uri = response.assets?.[0]?.uri;
        if (uri) await processUri(uri);
      },
    );
  }, [processUri]);

  // ─── Gallery pick ──────────────────────────────────────────────────────

  const handlePick = useCallback(async () => {
    launchImageLibrary(
      { mediaType: "photo", selectionLimit: 5 },
      async (response) => {
        if (response.didCancel) return;
        if (response.errorCode) {
          Alert.alert(
            "Gallery Error",
            response.errorMessage ?? response.errorCode,
          );
          return;
        }
        const uris = (response.assets ?? [])
          .map((a) => a.uri)
          .filter((u): u is string => !!u);
        for (const uri of uris) {
          await processUri(uri);
        }
      },
    );
  }, [processUri]);

  // ─── Utility ────────────────────────────────────────────────────────────

  const printLatestJsonToConsole = () => {
    if (results.length === 0) {
      Alert.alert("No Results", "Capture or pick an image first.");
      return;
    }
    console.log(
      "[DataLift][LatestResult]",
      JSON.stringify(results[0], null, 2),
    );
    Alert.alert("Printed", "Latest JSON printed in Metro console.");
  };

  const clearResults = () => {
    setResults([]);
    setError(null);
  };

  // ─── Render ─────────────────────────────────────────────────────────────

  return (
    <SafeAreaView style={styles.container}>
      <StatusBar barStyle="dark-content" backgroundColor="#f8fafc" />
      <ScrollView contentContainerStyle={styles.content}>
        <View style={styles.hero}>
          <Text style={styles.title}>DataLift Example</Text>
          <Text style={styles.subtitle}>
            Up to 5-stage pipeline: OCR → Parser → Confidence → LayoutLMv3 → AI
          </Text>
        </View>

        {!isDataLiftAvailable && (
          <View style={styles.warning}>
            <Text style={styles.warningText}>
              Native module not linked.{" "}
              {Platform.OS === "ios"
                ? "Run pod install + rebuild."
                : "Run a clean Android build."}
            </Text>
          </View>
        )}

        <View style={styles.card}>
          <Text style={styles.sectionTitle}>Document Extraction</Text>
          <Text style={styles.sectionText}>
            DataLift.extract() runs all stages automatically:{"\n"}
            1. Native OCR (Apple Vision / ML Kit){"\n"}
            2. Rule-based parser (payment, delivery, line items, UOM){"\n"}
            3. 5-factor confidence scoring + breakdown{"\n"}
            4. LayoutLMv3 on-device AI (auto-downloaded once){"\n"}
            5. Generic AI fallback (if confidence &lt; threshold)
          </Text>

          <View style={styles.statusRow}>
            <View
              style={[
                styles.statusDot,
                modelStatus.startsWith("ready")
                  ? styles.statusGreen
                  : styles.statusOrange,
              ]}
            />
            <Text style={styles.statusText}>{modelStatus}</Text>
          </View>
          {downloadProgress && (
            <View style={styles.progressBar}>
              <View
                style={[
                  styles.progressFill,
                  { width: `${downloadProgress.progress * 100}%` },
                ]}
              />
            </View>
          )}

          <View style={styles.buttonRow}>
            <TouchableOpacity
              style={[styles.button, loading && styles.buttonDisabled]}
              disabled={loading}
              onPress={handleCapture}
            >
              <Text style={styles.buttonText}>Capture</Text>
            </TouchableOpacity>
            <TouchableOpacity
              style={[
                styles.button,
                styles.buttonSecondary,
                loading && styles.buttonDisabled,
              ]}
              disabled={loading}
              onPress={handlePick}
            >
              <Text style={styles.buttonText}>Pick Images</Text>
            </TouchableOpacity>
          </View>

          {loading && (
            <View style={styles.loadingBox}>
              <ActivityIndicator color="#0ea5e9" />
              <Text style={styles.loadingText}>Extracting document…</Text>
            </View>
          )}

          {!!error && <Text style={styles.errorText}>{error}</Text>}
        </View>

        <View style={styles.resultsHeader}>
          <Text style={styles.resultsTitle}>Results ({results.length})</Text>
          <View style={styles.resultsActions}>
            <TouchableOpacity
              onPress={printLatestJsonToConsole}
              style={styles.consoleBtn}
            >
              <Text style={styles.consoleBtnText}>JSON Console</Text>
            </TouchableOpacity>
            <TouchableOpacity onPress={clearResults} style={styles.clearBtn}>
              <Text style={styles.clearBtnText}>Clear</Text>
            </TouchableOpacity>
          </View>
        </View>

        {results.length === 0 ? (
          <View style={styles.emptyState}>
            <Text style={styles.emptyTitle}>No output yet</Text>
            <Text style={styles.emptyText}>
              Capture or pick an image to run the unified extraction pipeline.
            </Text>
          </View>
        ) : (
          results.map((result, index) => (
            <ResultTile
              key={`${result.metadata?.processingTimeMs}-${index}`}
              result={result}
            />
          ))
        )}
      </ScrollView>
    </SafeAreaView>
  );
};

